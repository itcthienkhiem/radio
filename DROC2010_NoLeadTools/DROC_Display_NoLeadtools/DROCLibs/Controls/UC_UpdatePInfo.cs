    public partial class UC_UpdatePInfo : UserControl
    {
        DicomDataSet ds;
        DataTable dtPInfor;
        public delegate void OnUpdate(DataTable dt);
        public event OnUpdate _onUpdate;
        public UC_UpdatePInfo()
        {
            InitializeComponent();
            
        }
        DataTable InitTableStructure()
        {
            DataTable dt = new DataTable("PInfo");
            dt.Columns.AddRange(new DataColumn[] { new DataColumn("TagID", typeof(long)), new DataColumn("TagName", typeof(string)), new DataColumn("TagValue", typeof(string)) });
            return dt;
        }
        public void LoadPInfor(DicomDataSet ds)
        {
            try
            {
                this.ds = ds;
                dtPInfor = InitTableStructure();
                if (ds != null)
                {
                    AddRow("Mã BN(PatientID)", Leadtools.Dicom.DicomTag.PatientID);
                    AddRow("Tên BN(PatientName)", Leadtools.Dicom.DicomTag.PatientName);
                    AddRow("Tuổi(Age)", Leadtools.Dicom.DicomTag.PatientAge);
                    AddRow("Giới tính(Sex)", Leadtools.Dicom.DicomTag.PatientSex);
                    AddRow("Địa chỉ(Address)", Leadtools.Dicom.DicomTag.PatientAddress);
                    AddRow("Ngày sinh(BOD)", Leadtools.Dicom.DicomTag.PatientBirthDate);
                    AddRow("Giờ sinh(BOT)", Leadtools.Dicom.DicomTag.PatientBirthTime);
                    AddRow("Số đăng ký(Reg Sequence)", Leadtools.Dicom.DicomTag.RegistrationSequence);
                    AddRow("Modality", Leadtools.Dicom.DicomTag.Modality);
                }
                //Set datasource for datagridView
                Utility.SetDataSourceForDataGridView(grdList, dtPInfor, false, true, "1=1", "");

            }
            catch (Exception ex)
            {
                Utils.ShowMsg("Lỗi khi load thông tin ảnh:\n" + ex.Message, "Thông báo");
            }
        }
        void AddRow(string TagName, long tag)
        {
            DataRow dr = dtPInfor.NewRow();
            dr["TagID"] = tag;
            dr["TagName"] = TagName;
            if(tag== Leadtools.Dicom.DicomTag.PatientSex)
                dr["TagValue"] = TranslateSex(GetStringValue(tag));
            else
            dr["TagValue"] = GetStringValue(tag);
            dtPInfor.Rows.Add(dr);
        }
        string GetStringValue( long tag)
        {
            try
            {
                if (ds == null) return "";
                DicomElement element;
                element = ds.FindFirstElement(null, tag, false);
                if (element != null)
                {
                    if (ds.GetElementValueCount(element) > 0)
                    {
                        return ds.GetConvertValue(element);
                    }
                }
                return "";
            }
            catch
            {
                return "";
            }
        }
        string TranslateSex(string Sex)
        {
            if (Sex.ToUpper() == "M")
            {
                return "Nam";
            }
            if (Sex.ToUpper() == "F")
            {
                return "Nữ";
            }
            else
            {
                return "PD";
            }
        }
        private void cmdUpdate_Click(object sender, EventArgs e)
        {
            dtPInfor.AcceptChanges();
            _onUpdate(dtPInfor);
        }

        private void cmdReLoad_Click(object sender, EventArgs e)
        {
            try
            {
                if (dtPInfor != null && dtPInfor.GetChanges().Rows.Count > 0)
                    dtPInfor.RejectChanges();
            }
            catch
            {
            }
           
        }

        private void txtSearchTag_TextChanged(object sender, EventArgs e)
        {
            try
            {
                dtPInfor.DefaultView.RowFilter = "1=1";
                dtPInfor.DefaultView.RowFilter = "TagName like '*" + txtSearchTag.Text.Trim() + "*'";
            }
            catch
            {
            }
        }

        private void cmdOption_Click(object sender, EventArgs e)
        {

        }
    }
